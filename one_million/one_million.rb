module IntegerConverter

  WORDS_MAP = {
                1 => 'one',
                2 =>'two',
                3 => 'three',
                4 => 'four',
                5 => 'five',
                6 => 'six',
                7 => 'seven',
                8 => 'eight',
                9 => 'nine',
                10 => 'ten',
                11 => 'eleven',
                12 => 'twelve',
                13 => 'thirteen',
                14 => 'fourteen',
                15 => 'fifteen',
                16 => 'sixteen',
                17 => 'seventeen',
                18 => 'eighteen',
                19 => 'nineteen',
                20 => 'twenty',
                30 => 'thirty',
                40 => 'forty',
                50 => 'fifty',
                60 => 'sixty',
                70 => 'seventy',
                80 => 'eighty',
                90 => 'ninety'
              }

  def in_words
    WORDS_MAP[self] || in_multiple_words
  end

  def in_multiple_words
    (divmod(10)[0]*10).in_words + ' ' + divmod(10)[1].in_words
  end

end